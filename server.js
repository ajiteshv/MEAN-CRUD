/*
-----------------------------------------------------------------------------------
|
| Dependencies
|
-----------------------------------------------------------------------------------
*/

var express = require('express'),
    config = require(__dirname + '/config');

/*
-----------------------------------------------------------------------------------
|
| Server setup
|
-----------------------------------------------------------------------------------
*/

var server = express();
server.use(express.static(__dirname + '/public'));
server.listen(config.port);

/*
-----------------------------------------------------------------------------------
|
| Configure routes
|
-----------------------------------------------------------------------------------
*/

server.get('*', redirectToIndex);

/*
-----------------------------------------------------------------------------------
|
| Functions
|
-----------------------------------------------------------------------------------
*/

function redirectToIndex(req, res) {
  res.redirect('/');
}
